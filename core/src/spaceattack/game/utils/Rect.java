package spaceattack.game.utils;

public class Rect {

    private float x;
    private float y;
    private float width;
    private float height;

    private float red;
    private float green;
    private float blue;

    public float getX() {

        return x;
    }

    public void setX(float x) {

        this.x = x;
    }

    public float getY() {

        return y;
    }

    public void setY(float y) {

        this.y = y;
    }

    public float getWidth() {

        return width;
    }

    public void setWidth(float width) {

        this.width = width;
    }

    public float getHeight() {

        return height;
    }

    public void setHeight(float height) {

        this.height = height;
    }

    public float getRed() {

        return red;
    }

    public void setRed(float red) {

        this.red = red;
    }

    public float getGreen() {

        return green;
    }

    public void setGreen(float green) {

        this.green = green;
    }

    public float getBlue() {

        return blue;
    }

    public void setBlue(float blue) {

        this.blue = blue;
    }
}
